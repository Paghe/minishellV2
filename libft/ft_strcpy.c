#include "libft.h"

char	*ft_strcpy(char *s, char *s2)
{
	int	i;

	i = 0;
	if (!s)
		return (NULL);
	while (s2[i])
	{
		s[i] = s2[i];
		i++;
	}
	s[i] = '\0';
	return (s);
}
